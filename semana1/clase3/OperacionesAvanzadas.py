# division 
division = 40/13
print(division)

# division entera
divisioEntera = 40//13
print(divisioEntera)

# division de residuo (de modulo)
divisionResiduo = 40%13
print(divisionResiduo)

divisionResiduoPar = 10%2
print(divisionResiduoPar)

divisionResiduoImpar = 17%2
print(divisionResiduoImpar)

# potenciacion
potenciacion = 8**2
print(potenciacion)
